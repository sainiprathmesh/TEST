public class Latesh {
    String latesh;
    int age;

    public Latesh(String latesh, int age) {
        this.latesh = latesh;
        this.age = age;
    }

    public String getLatesh() {
        return latesh;
    }

    public void setLatesh(String latesh) {
        this.latesh = latesh;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
